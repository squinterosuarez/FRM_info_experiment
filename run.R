diag_lines <- c(diag_lines, sprintf("Blocks:          %d (%d tasks each)", N_BLOCKS, n_tasks))
diag_lines <- c(diag_lines, sprintf("Parameters:      %d", N_PAR))
diag_lines <- c(diag_lines, sprintf("Point D-error:   %.4f", FINAL_D_ERR))
diag_lines <- c(diag_lines, sprintf("Level imbalance: %.2f", FINAL_IMBAL))
diag_lines <- c(diag_lines, sprintf("Seed:            %d", SEED))
diag_lines <- c(diag_lines, "")

ab_rows <- full_design[full_design$alternative != "Status_Quo", ]

diag_lines <- c(diag_lines, "--- Level balance (A/B alternatives only) ---", "")

for (k in attr_keys) {
  a <- ATTRIBUTES[[k]]
  col <- paste0(k, "_level")
  freq <- table(ab_rows[[col]])
  diag_lines <- c(diag_lines, sprintf("%s (%s):", k, a$concept))
  for (i in seq_along(freq)) {
    lvl <- as.integer(names(freq)[i])
    pct <- 100 * freq[i] / sum(freq)
    lbl <- if (nchar(a$levels[lvl]) > 50) paste0(substr(a$levels[lvl], 1, 50), "...") else a$levels[lvl]
    diag_lines <- c(diag_lines, sprintf("  Level %d: n=%d (%.1f%%)  %s", lvl, freq[i], pct, lbl))
  }
  diag_lines <- c(diag_lines, "")
}

diag_lines <- c(diag_lines, "--- Level balance by block ---", "")
for (b in 1:N_BLOCKS) {
  block_rows <- ab_rows[ab_rows$block == b, ]
  diag_lines <- c(diag_lines, sprintf("Block %d (%d choice sets):", b, length(unique(block_rows$choice_set))))
  for (k in attr_keys) {
    col <- paste0(k, "_level")
    freq <- table(factor(block_rows[[col]]))
    diag_lines <- c(diag_lines, sprintf("  %s: %s", k,
                    paste(sprintf("L%s=%d", names(freq), freq), collapse=", ")))
  }
  diag_lines <- c(diag_lines, "")
}

# Dominance check
diag_lines <- c(diag_lines, "--- Dominance check ---")
n_dom <- 0
for (s in unique(full_design$choice_set)) {
  cs <- full_design[full_design$choice_set == s & full_design$alternative != "Status_Quo", ]
  same <- TRUE
  for (k in attr_keys) {
    if (k == cost_key) next
    if (cs[1, paste0(k, "_level")] != cs[2, paste0(k, "_level")]) { same <- FALSE; break }
  }
  if (same && cs[1, paste0(cost_key, "_value")] != cs[2, paste0(cost_key, "_value")]) {
    diag_lines <- c(diag_lines, sprintf("  WARNING: Set %d identical except cost", s))
    n_dom <- n_dom + 1
  }
}
if (n_dom == 0) diag_lines <- c(diag_lines, "  None found.")

# Attribute differences
diag_lines <- c(diag_lines, "", "--- Attribute differences (A vs B) ---")
diffs <- numeric(N_SETS)
for (i in seq_along(unique(full_design$choice_set))) {
  s <- unique(full_design$choice_set)[i]
  cs <- full_design[full_design$choice_set == s & full_design$alternative != "Status_Quo", ]
  n_diff <- sum(sapply(attr_keys, function(k) cs[1, paste0(k, "_level")] != cs[2, paste0(k, "_level")]))
  diffs[i] <- n_diff
}
diag_lines <- c(diag_lines,
  sprintf("  Min: %d  Max: %d  Mean: %.1f", min(diffs), max(diffs), mean(diffs)),
  sprintf("  Distribution: %s", paste(sprintf("%d-diff=%d", sort(unique(diffs)),
          table(diffs)[as.character(sort(unique(diffs)))]), collapse=", ")))

writeLines(diag_lines, file.path(OUTPUT_DIR, "design_diagnostics.txt"))
